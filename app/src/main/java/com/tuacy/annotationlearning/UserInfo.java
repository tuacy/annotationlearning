package com.tuacy.annotationlearning;

public class UserInfo {

	private String mName;
	private int    mAge;

	public String getName() {
		return mName;
	}

	public void setName(String name) {
		mName = name;
	}

	public int getAge() {
		return mAge;
	}

	public void setAge(int age) {
		mAge = age;
	}
}
